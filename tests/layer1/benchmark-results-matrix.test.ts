import { readFileSync } from "node:fs";
import { resolve } from "node:path";
import { describe, expect, it } from "vitest";

const repoRoot = resolve(import.meta.dirname, "../..");

describe("benchmark results matrix", () => {
  it("is generated from persisted benchmark reports and separates coverage from results", () => {
    const matrix = readFileSync(resolve(repoRoot, "docs/benchmark-results-matrix.md"), "utf8");

    expect(matrix).toContain("Generated by `scripts/generate-skills-showcase-data.mjs`");
    expect(matrix).toContain("`tests/harness/bench-coverage.ts`");
    expect(matrix).toContain(
      "| `ship` | Codex | `tests/benchmarks/runs/ship-codex-b69cb187/report.json` | 3 | 100% | 78.6% | `benchmark/review-ship-2026-05-11.md` | graded |",
    );
    expect(matrix).toContain(
      "| `affected` | Codex | `tests/benchmarks/runs/affected-codex-3c36c9a8/report.json` | blocked/incomplete | Report exists with zero total and evaluated runs. Do not count as benchmarked. |",
    );
    expect(matrix).toContain(
      "`commit-and-push-by-feature` and `sync` are currently blocked in the coverage registry",
    );
  });
});
